def _build_law_final_system_prompt(case_label: str, case_rules: str) -> str:
    return (
        "당신은 P&ID 정보에서 법규 위반 분석 최종 답변 작성자입니다.\n"
        f"케이스: {case_label}\n"
        f"{FORMAL_TONE_RULES}\n"
        f"{CITATION_FORMAT_RULES}\n"
        f"{case_rules}"
    )


LAW_FINAL_USER_TEMPLATE = """
[설비 정보]
{facility_info}

[근거 문서]
{law_docs_context}
""".strip() + "\n"


LAW_FINAL_PROMPTS = {
    "2-1": (
        _build_law_final_system_prompt("2-1 (법규 위반 YES)", _LAW_FINAL_RULES_2_1),
        LAW_FINAL_USER_TEMPLATE,
    ),
    "2-2": (
        _build_law_final_system_prompt("2-2 (법규 위반 NO)", _LAW_FINAL_RULES_2_2),
        LAW_FINAL_USER_TEMPLATE,
    ),
    "2-3": (
        _build_law_final_system_prompt("2-3 (법규 위반 판단 불가)", _LAW_FINAL_RULES_2_3),
        LAW_FINAL_USER_TEMPLATE,
    ),
}


def _resolve_law_prompt_key(case_code: str) -> str:
    case_code = str(case_code or "").strip()
    if "2-1" in case_code:
        return "2-1"
    if "2-2" in case_code:
        return "2-2"
    if "2-3" in case_code:
        return "2-3"
    return "2-3"


def get_law_final_prompt(case_code: str) -> tuple[str, str]:
    key = _resolve_law_prompt_key(case_code)
    return LAW_FINAL_PROMPTS[key]
